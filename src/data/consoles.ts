export function getConsoleBySlug(slug: string) {
  return consoles.find((c) => c.slug === slug);
}

export function sortByEmotional() {
  return [...consoles].sort((a, b) => b.emotionalScore - a.emotionalScore);
}

export function sortByRarity() {
  return [...consoles].sort((a, b) => b.rarityScore - a.rarityScore);
}

export function sortByYear() {
  return [...consoles].sort((a, b) => a.year - b.year);
}
